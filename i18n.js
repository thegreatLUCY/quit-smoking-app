let currentLang = localStorage.getItem('qt_lang') || 'en';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('qt_lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) el.textContent = translations[lang][key];
  });
  updateToggleButtons();
}

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || (translations['en'] && translations['en'][key]) || key;
}

function fmtNum(n) {
  return new Intl.NumberFormat(currentLang === 'ar' ? 'ar-EG' : 'en-US').format(n);
}

function updateToggleButtons() {
  document.querySelectorAll('.toggle-btn[data-lang]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
  const theme = document.documentElement.dataset.theme || 'dark';
  document.querySelectorAll('.toggle-btn[data-theme]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.theme === theme);
  });
}
